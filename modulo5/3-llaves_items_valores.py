# Llaves, items y valores

diccionario = {'a':1, 'b':2, 'c':3, 'd':4,}

# Con la ayuda de los siguientes metodos, podremos obtener los diferentes valores dentro de tuplas, las cuales son inmutables y solo para lectura.

# keys
llaves = diccionario.keys()
print(llaves)

# values
valores = diccionario.values()
print(valores)

# items
objetos = diccionario.items()
print(objetos)
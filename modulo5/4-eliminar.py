# Eliminar

diccionario = {'a':1, 'b':2, 'c':3, 'd':4,}

# Palabra reservada del
del diccionario['a']
print(diccionario)

# metodo pop() - recibe el elemento a extraer del diccionario y retorna el valor removido.
diccionario.pop('c')
print(diccionario)

# Eliminar todos los elementos con el metodo clear()
diccionario.clear()
print(diccionario)
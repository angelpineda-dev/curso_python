# Operadores relacionales

'''
- Nos permiten obtener un valor booleano (True o False)
 > mayor que
 < menor que
 >= mayor o igual que
 <= menor o igual que
 == igual que
 != diferente de
'''

numero_uno = 10
numero_dos = 20

resultado = numero_uno >= numero_dos
print(resultado)

resultado = numero_uno <= numero_dos
print(resultado)

resultado = numero_uno == numero_dos
print(resultado)

resultado = numero_uno != numero_dos
print(resultado)


